const levelThresholds = [
  { max: 119, level: "easy" },
  { max: 209, level: "normal" },
  { max: 269, level: "green" },
  { max: Number.POSITIVE_INFINITY, level: "blue" },
];

const exampleTemplates = {
  noun: (word, meaning, categoryZh) => ({
    example: `The team reviewed the ${word} during today's study session.`,
    exampleZh: `團隊今天在${categoryZh}練習中檢查了這個「${meaning}」。`,
  }),
  verb: (word, meaning) => ({
    example: `Please ${word} the document before the deadline.`,
    exampleZh: `請在截止前先${meaning}這份文件。`,
  }),
  adjective: (word, meaning) => ({
    example: `The ${word} update helped everyone make a faster decision.`,
    exampleZh: `這則${meaning}的更新幫助大家更快做出決定。`,
  }),
};

const fallbackCollocations = {
  noun: (word) => [`review the ${word}`, `${word} update`],
  verb: (word) => [`${word} the request`, `${word} the schedule`],
  adjective: (word) => [`${word} report`, `${word} notice`],
};

const pickLevel = (index) =>
  levelThresholds.find((rule) => index <= rule.max)?.level ?? "blue";

const buildPronunciation = (word) => `/${word.replace(/\s+/g, " ")} /`;

const seedEntries = categoryBlueprints.flatMap((category) =>
  category.seeds.map((seed) => ({
    ...seed,
    category: category.label,
    categoryZh: category.labelZh,
  })),
);

export const vocabularyBank = seedEntries.map((seed, index) => {
  const level = pickLevel(index);
  const template = exampleTemplates[seed.partOfSpeech] ?? exampleTemplates.noun;
  const { example, exampleZh } = template(seed.word, seed.meaning, seed.categoryZh);

  return {
    id: index + 1,
    word: seed.word,
    meaning: seed.meaning,
    partOfSpeech: seed.partOfSpeech,
    pronunciation: buildPronunciation(seed.word),
    example,
    exampleZh,
    collocations:
      seed.collocations.length > 0
        ? seed.collocations
        : fallbackCollocations[seed.partOfSpeech](seed.word),
    category: seed.category,
    categoryZh: seed.categoryZh,
    level,
    frequency: 5 - (index % 5),
    isFavorite: false,
    wrongCount: 0,
    mastered: false,
  };
});

